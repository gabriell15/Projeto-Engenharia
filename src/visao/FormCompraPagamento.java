    public void preencherTabela(String Sql){
        ArrayList dados= new ArrayList();
        String [] colunas= new String []{"CNPJ", "ID" ,  "Título", "Valor"};
        
        conex.conexao();
        conex.executaSql(Sql);
        
        try {
            conex.rs.first();
            
            do{
                dados.add(new Object[]{conex.rs.getString("cnpj"),conex.rs.getString("id_jogo"), conex.rs.getString("titulo_jogo"),conex.rs.getDouble("valor")});
                
            }while(conex.rs.next());
            
        } catch (SQLException ex) {
            
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher o ArrayList"+ex);
        }
        
        ModeloTabela modelo= new ModeloTabela(dados, colunas);
        
        jTableEscolhaJogo.setModel(modelo);
        jTableEscolhaJogo.getColumnModel().getColumn(0).setPreferredWidth(150);
        
        //Empresa nao vai poder mecher com o tamanho da tabela com o mause:
        jTableEscolhaJogo.getColumnModel().getColumn(0).setResizable(false);
        
        jTableEscolhaJogo.getColumnModel().getColumn(1).setPreferredWidth(150);
        jTableEscolhaJogo.getColumnModel().getColumn(1).setResizable(false);
        jTableEscolhaJogo.getColumnModel().getColumn(2).setPreferredWidth(150);
        jTableEscolhaJogo.getColumnModel().getColumn(2).setResizable(false);
        jTableEscolhaJogo.getColumnModel().getColumn(3).setPreferredWidth(150);
        jTableEscolhaJogo.getColumnModel().getColumn(3).setResizable(false);
     
      

        
        //Aqui o empresa nao vai poder reordenar o cabeçalho:
        jTableEscolhaJogo.getTableHeader().setReorderingAllowed(false);
        
        //Empresa nao vai poder redimensionar a tabela:
        jTableEscolhaJogo.setAutoResizeMode(jTableEscolhaJogo.AUTO_RESIZE_OFF);
        
        //Empresa so vai poder selecionar um campo por vez ao clicar na tabela:
        jTableEscolhaJogo.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
         
        
        
        
        conex.desconecta();
        
    }
        
 
    
    
