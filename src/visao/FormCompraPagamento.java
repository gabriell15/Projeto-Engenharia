    private void jTableEscolhaJogoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableEscolhaJogoMouseClicked
        // TODO add your handling code here:

        //Preencher os campos em cima

        String nome= ""+jTableEscolhaJogo.getValueAt(jTableEscolhaJogo.getSelectedRow(), 1);
       //int numero = (int) jTableEscolhaJogo.getValueAt(jTableEscolhaJogo.getSelectedRow(), 1);

        conex.conexao();

        conex.executaSql("select *from jogo where id_jogo = '"+nome+"' ");

        try {
            conex.rs.first();

         
            jTextFieldTitulo.setText(conex.rs.getString("titulo_jogo"));
            jTextFieldValor.setText(String.valueOf(conex.rs.getInt("valor")));
            jTextFieldId.setText(conex.rs.getString("id_jogo"));
           

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao selecionar os dados"+ex);
        }
        
        
        jTextFieldCvv.setEnabled(true);
        jTextFieldNome.setEnabled(true);
        jFormattedTextFieldData.setEnabled(true);
        jTextFieldNumero.setEnabled(true);

        conex.desconecta();
      
    }//GEN-LAST:event_jTableEscolhaJogoMouseClicked

