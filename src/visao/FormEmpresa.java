    private void jTableEmpresaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableEmpresaMouseClicked
        // TODO add your handling code here:

        //Preencher os campos em cima

        String emailEmpresa= ""+jTableEmpresa.getValueAt(jTableEmpresa.getSelectedRow(), 1);

        conex.conexao();

        conex.executaSql("select *from empresa where email_empresa = '"+emailEmpresa+"' ");

        try {
            conex.rs.first();
            jTextFieldEmail.setText(conex.rs.getString("email_empresa"));
            jFormattedTextFieldCNPJ.setText(conex.rs.getString("cnpj"));
            
            jPasswordFieldSenha.setText(conex.rs.getString("senha_empresa"));  
            jTextFieldNome.setText(conex.rs.getString("nome_empresa"));
           

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao selecionar os dados"+ex);
        }

        conex.desconecta();
        jButtonEditar.setEnabled(true);
        jButtonExcluir.setEnabled(true);

    }//GEN-LAST:event_jTableEmpresaMouseClicked

