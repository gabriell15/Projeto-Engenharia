    public void preencherTabela(String Sql){
        ArrayList dados= new ArrayList();
        String [] colunas= new String []{"CNPJ", "Email" ,  "Senha", "Nome"};
        
        conex.conexao();
        conex.executaSql(Sql);
        
        try {
            conex.rs.first();
            
            do{
                dados.add(new Object[]{conex.rs.getString("cnpj"),conex.rs.getString("email_empresa"), conex.rs.getString("senha_empresa"),conex.rs.getString("nome_empresa")});
                
            }while(conex.rs.next());
            
        } catch (SQLException ex) {
            
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher o ArrayList"+ex);
        }
        
        ModeloTabela modelo= new ModeloTabela(dados, colunas);
        
        jTableEmpresa.setModel(modelo);
        jTableEmpresa.getColumnModel().getColumn(0).setPreferredWidth(50);
        
        //Empresa nao vai poder mecher com o tamanho da tabela com o mause:
        jTableEmpresa.getColumnModel().getColumn(0).setResizable(false);
        
        jTableEmpresa.getColumnModel().getColumn(1).setPreferredWidth(100);
        jTableEmpresa.getColumnModel().getColumn(1).setResizable(false);
        jTableEmpresa.getColumnModel().getColumn(2).setPreferredWidth(100);
        jTableEmpresa.getColumnModel().getColumn(2).setResizable(false);
        jTableEmpresa.getColumnModel().getColumn(3).setPreferredWidth(100);
        jTableEmpresa.getColumnModel().getColumn(3).setResizable(false);
     
      

        
        //Aqui o empresa nao vai poder reordenar o cabeçalho:
        jTableEmpresa.getTableHeader().setReorderingAllowed(false);
        
        //Empresa nao vai poder redimensionar a tabela:
        jTableEmpresa.setAutoResizeMode(jTableEmpresa.AUTO_RESIZE_OFF);
        
        //Empresa so vai poder selecionar um campo por vez ao clicar na tabela:
        jTableEmpresa.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
         
        
        
        
        conex.desconecta();
        
    }
    
    
