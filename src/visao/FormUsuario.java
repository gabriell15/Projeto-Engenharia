    public void preencherTabela(String Sql){
        ArrayList dados= new ArrayList();
        String [] colunas= new String []{"CPF", "Nome" ,  "Senha", "Email"};
        
        conex.conexao();
        conex.executaSql(Sql);
        
        try {
            conex.rs.first();
            
            do{
                dados.add(new Object[]{conex.rs.getString("cpf"),conex.rs.getString("nome_user"), conex.rs.getString("senha"),conex.rs.getString("email")});
                
            }while(conex.rs.next());
            
        } catch (SQLException ex) {
            
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher o ArrayList"+ex);
        }
        
        ModeloTabela modelo= new ModeloTabela(dados, colunas);
        
        jTableUsuarios.setModel(modelo);
        jTableUsuarios.getColumnModel().getColumn(0).setPreferredWidth(150);
        
        //Usuario nao vai poder mecher com o tamanho da tabela com o mause:
        jTableUsuarios.getColumnModel().getColumn(0).setResizable(false);
        
        jTableUsuarios.getColumnModel().getColumn(1).setPreferredWidth(150);
        jTableUsuarios.getColumnModel().getColumn(1).setResizable(false);
        jTableUsuarios.getColumnModel().getColumn(2).setPreferredWidth(150);
        jTableUsuarios.getColumnModel().getColumn(2).setResizable(false);
        jTableUsuarios.getColumnModel().getColumn(3).setPreferredWidth(150);
        jTableUsuarios.getColumnModel().getColumn(3).setResizable(false);
     
      

        
        //Aqui o usuario nao vai poder reordenar o cabeçalho:
        jTableUsuarios.getTableHeader().setReorderingAllowed(false);
        
        //Usuario nao vai poder redimensionar a tabela:
        jTableUsuarios.setAutoResizeMode(jTableUsuarios.AUTO_RESIZE_OFF);
        
        //Usuario so vai poder selecionar um campo por vez ao clicar na tabela:
        jTableUsuarios.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
         
        
        
        
        conex.desconecta();
        
    }
    
    
