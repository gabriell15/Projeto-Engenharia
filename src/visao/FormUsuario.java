    private void jTableUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableUsuariosMouseClicked
        // TODO add your handling code here:

        //Preencher os campos em cima

        String cpf_Usuario= ""+jTableUsuarios.getValueAt(jTableUsuarios.getSelectedRow(), 1);

        conex.conexao();

        conex.executaSql("select *from pessoa_f where nome_user= '"+cpf_Usuario+"' ");

        try {
            conex.rs.first();
            jFormattedTextFieldCpf.setText(conex.rs.getString("cpf"));
            jPasswordFieldSenha.setText(conex.rs.getString("senha"));
            jTextFieldEmail.setText(conex.rs.getString("email"));
            jTextFieldNome.setText(conex.rs.getString("nome_user"));
           

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao selecionar os dados"+ex);
        }

        conex.desconecta();
     
    

    }//GEN-LAST:event_jTableUsuariosMouseClicked

